#!/usr/bin/env node
// Generate TypeScript types from FandomForge JSON schemas.
//
// Reads every `*.schema.json` under tools/fandomforge/schemas/ and writes a single
// TypeScript file at web/src/lib/types/generated.ts with one exported interface or
// type alias per schema. Also emits an `ArtifactSchemaMap` for convenience.
//
// Zero-dep on purpose. Our schemas are small and tree-shaped (internal $ref within
// $defs only, no cross-file refs).
//
// Usage:
//   node scripts/generate-ts-types.mjs
//
// Run after any schema change. Wired into `pnpm types:gen` in web/package.json.

import { readFile, readdir, writeFile, mkdir } from "node:fs/promises";
import { join, dirname, basename } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = join(__dirname, "..");
const SCHEMA_DIR = join(REPO_ROOT, "tools", "fandomforge", "schemas");
const OUT = join(REPO_ROOT, "web", "src", "lib", "types", "generated.ts");

function pascal(s) {
  return s
    .split(/[-_\s]/)
    .filter(Boolean)
    .map((p) => p[0].toUpperCase() + p.slice(1))
    .join("");
}

function tsLiteral(v) {
  if (v === null) return "null";
  if (typeof v === "string") return JSON.stringify(v);
  if (typeof v === "number" || typeof v === "boolean") return String(v);
  return "unknown";
}

function renderType(schema, ctx) {
  if (!schema || typeof schema !== "object") return "unknown";

  if (schema.$ref) {
    const m = /^#\/\$defs\/(.+)$/.exec(schema.$ref);
    if (m) return pascal(m[1]);
    return "unknown";
  }

  if (schema.const !== undefined) return tsLiteral(schema.const);

  if (Array.isArray(schema.enum)) {
    return schema.enum.map(tsLiteral).join(" | ") || "never";
  }

  if (schema.anyOf || schema.oneOf) {
    const members = schema.anyOf ?? schema.oneOf;
    return members.map((m) => renderType(m, ctx)).join(" | ") || "unknown";
  }

  if (schema.allOf) {
    return schema.allOf.map((m) => renderType(m, ctx)).join(" & ") || "unknown";
  }

  if (Array.isArray(schema.type)) {
    return schema.type.map((t) => renderType({ ...schema, type: t }, ctx)).join(" | ");
  }

  const t = schema.type;
  if (t === "string") return "string";
  if (t === "integer" || t === "number") return "number";
  if (t === "boolean") return "boolean";
  if (t === "null") return "null";

  if (t === "array") {
    const inner = renderType(schema.items ?? {}, ctx);
    return `Array<${inner}>`;
  }

  if (t === "object" || schema.properties || schema.additionalProperties) {
    return renderObject(schema, ctx);
  }

  return "unknown";
}

function renderObject(schema, ctx) {
  const props = schema.properties ?? {};
  const required = new Set(schema.required ?? []);
  const keys = Object.keys(props);

  const rows = keys.map((k) => {
    const pSchema = props[k];
    const opt = required.has(k) ? "" : "?";
    const value = renderType(pSchema, ctx);
    const doc = pSchema.description
      ? `    /** ${pSchema.description.replace(/\*\//g, "* /")} */\n`
      : "";
    const quoted = /^[a-zA-Z_$][\w$]*$/.test(k) ? k : JSON.stringify(k);
    return `${doc}    ${quoted}${opt}: ${value};`;
  });

  const ap = schema.additionalProperties;
  if (ap && typeof ap === "object") {
    const apType = renderType(ap, ctx);
    if (rows.length > 0) {
      return `{\n${rows.join("\n")}\n    [key: string]: ${apType} | ${
        keys.map((k) => renderType(props[k], ctx)).join(" | ") || "unknown"
      };\n  }`;
    }
    return `Record<string, ${apType}>`;
  }

  if (rows.length === 0) return "Record<string, unknown>";
  return `{\n${rows.join("\n")}\n  }`;
}

function renderDefs(schema) {
  const defs = schema.$defs ?? {};
  const out = [];
  for (const [name, def] of Object.entries(defs)) {
    const ty = renderType(def, { rootTitle: schema.title });
    const doc = def.description ? `/** ${def.description} */\n` : "";
    out.push(`${doc}export type ${pascal(name)} = ${ty};`);
  }
  return out.join("\n\n");
}

async function generate() {
  const entries = await readdir(SCHEMA_DIR);
  const schemaFiles = entries
    .filter((f) => f.endsWith(".schema.json"))
    .sort();

  const parts = [];
  const artifactIds = [];

  parts.push(`/* eslint-disable */
// THIS FILE IS GENERATED BY scripts/generate-ts-types.mjs
// DO NOT EDIT BY HAND. Run \`pnpm types:gen\` after any schema change.
//
// Source of truth: tools/fandomforge/schemas/*.schema.json
// Generated at: ${new Date().toISOString()}
`);

  for (const file of schemaFiles) {
    const id = basename(file, ".schema.json");
    artifactIds.push(id);
    const raw = await readFile(join(SCHEMA_DIR, file), "utf8");
    const schema = JSON.parse(raw);
    const typeName = pascal(id);
    const doc = schema.description ? `/** ${schema.description} */\n` : "";

    const defsBlock = renderDefs(schema);
    if (defsBlock) parts.push(defsBlock);

    const tsType = renderType(schema, { rootTitle: schema.title ?? typeName });
    parts.push(`${doc}export interface ${typeName} ${tsType}`);
  }

  const mapEntries = artifactIds
    .map((id) => `  ${JSON.stringify(id)}: ${pascal(id)};`)
    .join("\n");
  parts.push(`export interface ArtifactSchemaMap {\n${mapEntries}\n}`);
  parts.push(
    `export type ArtifactSchemaId = keyof ArtifactSchemaMap;`
  );
  parts.push(
    `export const ARTIFACT_SCHEMA_IDS: readonly ArtifactSchemaId[] = [${artifactIds
      .map((id) => JSON.stringify(id))
      .join(", ")}] as const;`
  );

  const content = parts.join("\n\n") + "\n";

  await mkdir(dirname(OUT), { recursive: true });
  await writeFile(OUT, content, "utf8");
  console.log(`Wrote ${OUT} with ${artifactIds.length} schemas.`);
}

generate().catch((err) => {
  console.error(err);
  process.exit(1);
});
